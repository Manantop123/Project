package com.loop;

public class loop {
	public static void main(String[] args) {
		int a=9;
		
		System.out.println("while loop");
		
		while(a<=10)
		{
			System.out.println("manan");
			a++;
		}
		System.out.println("do-while loop ");
		
		do
		{
			System.out.println("manan");
			a++;
		}
		while(a<=10);
	}
		
}
